class LanguageManager {
    constructor() {
        this.currentLang = localStorage.getItem('btc_season3_lang') ||
            (navigator.language.startsWith('zh') ? 'zh' : 'en');
        this.init();
    }

    init() {
        this.updateButtons();
        this.translatePage();
        this.applyLanguageToBody();
        this.initSplashScreen();
    }

    initSplashScreen() {
        // Ensure splash screen stays for at least 800ms for premium feel
        const minLoadTime = 800;
        const startLoad = Date.now();

        window.addEventListener('load', () => {
            const elapsed = Date.now() - startLoad;
            const remaining = Math.max(0, minLoadTime - elapsed);

            setTimeout(() => {
                const splash = document.getElementById('splash-screen');
                if (splash) {
                    splash.classList.add('hidden');

                    // Trigger hero animations after splash fade
                    setTimeout(() => {
                        document.querySelectorAll('.hero-section, .navbar, .mint-section').forEach(el => {
                            el.classList.add('active'); // Ensure they are visible
                        });
                    }, 400);
                }
            }, remaining);
        });
    }

    setLanguage(lang) {
        this.currentLang = lang;
        localStorage.setItem('btc_season3_lang', lang);
        this.init();
    }

    applyLanguageToBody() {
        document.documentElement.lang = this.currentLang;
    }

    updateButtons() {
        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.classList.toggle('active', btn.dataset.lang === this.currentLang);
            btn.addEventListener('click', () => this.setLanguage(btn.dataset.lang), { once: true });
        });
    }

    translatePage() {
        // Update standard elements
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(el => {
            const key = el.getAttribute('data-i18n');
            let translation = translations[this.currentLang][key];

            if (translation) {
                const vars = el.getAttribute('data-i18n-vars');
                if (vars) {
                    const varObj = JSON.parse(vars.replace(/'/g, '"'));
                    Object.keys(varObj).forEach(vKey => {
                        translation = translation.replace(`{${vKey}}`, varObj[vKey]);
                    });
                }
                el.innerHTML = translation;
            }
        });

        // Update meta tags and title
        this.updateMetaTags();
    }

    updateMetaTags() {
        const langData = translations[this.currentLang];
        if (!langData) return;

        // Update Title
        let titleKey = '';
        if (document.body.classList.contains('mode-nakamoto-goat')) titleKey = 'meta_title_nakamoto';
        else if (document.body.classList.contains('mode-space-goat')) titleKey = 'meta_title_space';
        else if (document.body.classList.contains('mode-queen-goat')) titleKey = 'meta_title_queen';

        if (titleKey && langData[titleKey]) {
            document.title = langData[titleKey];
        }

        // Update Description
        const metaDesc = document.querySelector('meta[name="description"]');
        if (metaDesc && langData.meta_description) metaDesc.setAttribute('content', langData.meta_description);

        const ogDesc = document.querySelector('meta[property="og:description"]');
        if (ogDesc && langData.meta_description) ogDesc.setAttribute('content', langData.meta_description);

        const twDesc = document.querySelector('meta[property="twitter:description"]');
        if (twDesc && langData.meta_description) twDesc.setAttribute('content', langData.meta_description);

        // Update titles
        if (titleKey && langData[titleKey]) {
            const ogTitle = document.querySelector('meta[property="og:title"]');
            if (ogTitle) ogTitle.setAttribute('content', langData[titleKey]);

            const twTitle = document.querySelector('meta[property="twitter:title"]');
            if (twTitle) twTitle.setAttribute('content', langData[titleKey]);
        }
    }
}

